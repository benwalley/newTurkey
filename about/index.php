		<h1>About Us</h1>
      <div></div>
        <p>The concept of "Borrow My Angel" originated with a song written to my sister, Charity Dow, when she was in her darkest place as she battled with mental illness. While walking through my sister’s journey with her, I realized that there is no such thing as “too many allies” in the life of someone struggling with mental illness.</p>

<p>When she was at one of her lowest moments, I struggled to convey to her how I felt, and how badly I wanted to help bear her burdens. Those emotions were channeled into the song, "Borrow My Angel" letting her know that any time she needed it, she could borrow my guardian angel to help her overcome the challenges she faced living with mental illness.</p>

<p>The Borrow My Angel Foundation embodies a vision that celebrates the power of a listening ear and an open heart.  Our goal is to mobilize an army of volunteers who are willing to simply be a friend, a non-judgmental voice who offers an ear, a pathway to hope and recovery, and resources for guidance to those that are struggling with mental illness, depression or suicidal thoughts when they need it the most.  </p>
        <h2> Mission Statement</h2>
      <p>The official mission of The Borrow My Angel Foundation is two fold:</p>

<p>1.) Increase awareness around mental disorders and mental disease. We must understand that those who are suffering have a DISEASE and truly need HELP. Culturally, we would never dream of telling someone with cancer that they need to “suck it up” or “get over it”. Yet that is far too often the message that our society sends those who are in need of help.</p>

<p>2.) Offer help and hope to those who are in their darkest moments. By leveraging volunteers and technology, our vision is to provide an easy, anonymous and safe method for those who are struggling to reach out for hope, an ear to listen, and resources for help and assistance.</p>
		
